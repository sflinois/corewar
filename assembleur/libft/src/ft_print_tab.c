#include "libft.h"

void	print_tab(char **tab)
{
	int i;

	i = 0;
	while (tab[i])
	{
		ft_putstr_fd("---------------------------->>>", 2);
		ft_putstr_fd(tab[i], 2);
		ft_putstr_fd("<=====", 2);
		ft_putnbr_fd(i, 2);
		ft_putstr_fd("\n", 2);
		i++;
	}
}
