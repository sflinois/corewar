#include "vm.h"

void	ft_fork(t_vm *vm, t_list_cpu *cpu)
{
	int			ind;

	ind = indirect(vm, cpu, 0);
	add_new_cpu(vm, cpu, (cpu->pc + ind) % MEM_SIZE);
}
